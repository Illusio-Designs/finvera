/**
 * Get notification configuration by type
 * @param {string} type - Notification type
 * @returns {Object} Configuration object
 */
export function getNotificationConfig(type) {
  return NOTIFICATION_CONFIG[type] || {
    category: 'info',
    icon: 'â„¹ï¸',
    color: '#3b82f6',
    bgColor: '#dbeafe',
    borderColor: '#3b82f6',
    duration: 4000,
    priority: NOTIFICATION_PRIORITY.MEDIUM,
  };
}

/**
 * Get all notification types by category
 * @param {string} category - Category (success, error, warning, info)
 * @returns {Array} Array of notification types
 */
export function getNotificationsByCategory(category) {
  return Object.entries(NOTIFICATION_CONFIG)
    .filter(([_, config]) => config.category === category)
    .map(([type]) => type);
}
